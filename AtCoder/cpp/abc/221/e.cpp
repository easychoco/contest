using mint = modint998244353;

void solve()
{
  ll n;
  cin >> n;
  vector<P> ap(n);
  rep(i, n)
  {
    ll a;
    cin >> a;
    ap[i] = P(a, i);
  }
  sort(ALL(ap));

  mint inv2 = mint(2).inv();
  fenwick_tree<mint> fw(n);

  mint ans = 0;
  rep(i, n)
  {
    auto [val, idx] = ap[i];
    ans += fw.sum(0, idx) * mint(2).pow(idx);
    fw.add(idx, inv2.pow(idx + 1));
  }
  show(ans.val());
}

