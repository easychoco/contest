using mint = modint998244353;

// a^n % mod を計算する O(log n)
ll powmod(ll a, ll n, ll mod) {
  ll ret = 1;
  while (n > 0) {
    if (n % 2 == 1) { ret = ret * a % mod; }
    a = a * a % mod;
    n /= 2;
  }
  return ret;
}

void solve()
{
  long long n, k, m;
  cin >> n >> k >> m;

  if (m % 998244353 == 0)
  {
    show(0);
    return;
  }

  // 答えは m ^ (k ^ n) % 998244353 だけど、(k ^ n) を mod とると答えがずれる
  ll p = powmod(k, n, 998244352);

  mint ans = mint(m).pow(p);
  show(ans.val());
}

